export function normalizeTickerInput(value: string) {
  return value.trim().toUpperCase();
}

export function buildTickerSuggestions(extra: string[] = [], limit = 60) {
  const merged = Array.from(
    new Set([
      ...extra.map((ticker) => normalizeTickerInput(ticker)),
      ...POPULAR_TICKERS
    ])
  ).filter(Boolean);
  return merged.slice(0, limit);
}
